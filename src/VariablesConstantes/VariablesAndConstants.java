package VariablesConstantes;

public class VariablesAndConstants {
    public static void main(String[] args) {
        // Variables
        String name = "Brayan"; // type string
        name = "Jhair"; // Actualizamos el contenido

        int age = 20; // type integer

        var lastname = "Bejar";

        // Constantes => final
        final String EMAIL = "bejarramos@gmail.com";

        System.out.println(name + age + lastname + EMAIL);
    }
}
